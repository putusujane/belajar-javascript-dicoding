// switch case Statement

let language = "French";

let greeting = null;

switch (language) {
    case "English":
        greeting = "Hello";
        break;
    case "French":
        greeting = "Bonjour";
        break;
    case "Italia":
        greeting = "Holla";
        break;
    case "Indonesia":
        greeting = "Halo!";
        break;
    default:
        greeting = "Lorem ipsum";
}

console.log(greeting);
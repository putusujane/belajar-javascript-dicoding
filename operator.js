// Operator

// Operator shortcut

let x = 7;
let y = 5;

// Tambah
x += y;
console.log(x);

// Kurang
x -= y;
console.log(x);

// Kali
x *= y;
console.log(x);

// Bagi
x /= y;
console.log(x);

// Pangkat
x **= y;
console.log(x);

// Modulus
x %= y;
console.log(x);

// Operator perbandingan
